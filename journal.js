// Greet a user by name
function greet(name) {
    return "Hello, " + name + "!";
}

// Ask a user's age
function fetchAge(age) {
    return "You are " + age + "years old!";
}

// Calculate age in 20 years
function calcAge(age){
    aged = age + 20
    return "In 20 years, you will be " + aged + "years old";
}
